private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
    Object objAmbiente = jComboAmbiente.getSelectedItem();
    Object objPapel = jComboPapel.getSelectedItem();
    String nomeAgente = txtNomeAgente.getText();
    if (!stringInvalida(nomeAgente, "O NOME do agente é inválido")) {
        return;
    }
    if (!(objAmbiente instanceof Ambiente)) {
        stringInvalida(null, "Selecione um Ambiente");
        return;
    }
    if (!(objPapel instanceof AgentRole)) {
        stringInvalida(null, "Selecione um Papel");
        return;
    }

    if (FabricaAgente.existeAgente(nomeAgente + ":" + objPapel, (Ambiente) objAmbiente)) {
        stringInvalida(null, "Já existe esse agente no ambiente selecionado");
        return;
    }

    Agent a = GeradorAgentes.gerarAgente(nomeAgente, (AgentRole) objPapel, (Ambiente) objAmbiente, Main.OrganizacaoPrincipal);
    agentes.add(a);
    jListAgentes.setListData(agentes);
}//GEN-LAST:event_btnAdicionarActionPerformed

    private void adicionaNoComboBox(JComboBox combo, Object objeto) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) combo.getModel();
        model.addElement(objeto);
        combo.setSelectedIndex(0);
    }
private void btnAdicionar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionar2ActionPerformed
// TODO add your handling code here:
    Ambiente ambiente = null;
    String nomeAmbiente = txtnomeAmbiente.getText();
    if (!stringInvalida(nomeAmbiente, "Preencha corretamente o NOME do ambiente")) {
        return;
    }
    if (FabricaAmbiente.existeAmbiente(nomeAmbiente)) {
        stringInvalida(null, "Ambiente já criado");
        return;
    }

    ambiente = FabricaAmbiente.getAmbiente(nomeAmbiente, JComboTipoAmbiente.getSelectedItem().toString());
    ambientes.add(ambiente);
    jListAmbiente.setListData(ambientes);
    adicionaNoComboBox(jComboAmbiente, ambiente);
}//GEN-LAST:event_btnAdicionar2ActionPerformed

